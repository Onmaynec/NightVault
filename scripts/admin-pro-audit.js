"use strict";
const fs = require("fs");
const path = require("path");
const root = path.join(__dirname, "..");
const read = (file) => fs.readFileSync(path.join(root, file), "utf8");
const pkg = JSON.parse(read("package.json"));
const admin = read("src/admin-renderer.js");
const main = read("src/main.js");
const server = read("server/server.js");
const css = read("src/admin.css");
const checks = [];
function check(name, ok){ checks.push({ name, ok:Boolean(ok) }); console.log(`${ok ? "OK" : "FAIL"} ${name}`); }
check("version 1.4.2", pkg.version === "1.4.2");
check("admin dashboard", admin.includes("renderDashboard") && admin.includes("Dashboard") && admin.includes("metricsGrid"));
check("radmin helper", admin.includes("Radmin/LAN Helper 2.0") && main.includes("NIGHTVAULT_HOST") && main.includes("0.0.0.0"));
check("console 2.0", admin.includes("Admin Console 2.0") && admin.includes("commandHistory") && admin.includes("autocompleteCommand"));
check("log filters", admin.includes("Log Center 2.0") && admin.includes("state.logFilter") && admin.includes("polling"));
check("users manager", admin.includes("renderUsers") && admin.includes("Users Manager"));
check("sessions devices manager", admin.includes("renderSessions") && admin.includes("Sessions & Devices Manager"));
check("backup manager v2", admin.includes("Backup Manager 2.0") && main.includes("nightvault-backup") && main.includes("checksum"));
check("database viewer 2.0", admin.includes("Database Viewer 2.0") && admin.includes("Export CSV"));
check("test center 2.0", admin.includes("Test Center 2.0") && admin.includes("websocket") && admin.includes("release"));
check("server health monitor", main.includes("adminSnapshot138") && main.includes("requestsPerMinute") && server.includes("getRuntimeMetrics"));
check("broadcast system", main.includes("sendBroadcast138") && server.includes("broadcastAdminEvent"));
check("maintenance mode", main.includes("admin-maintenance-set") && server.includes("__nightVaultMaintenance"));
check("admin themes 2.0", css.includes("data-admin-theme") && admin.includes("Admin Themes 2.0"));
check("release/update center", admin.includes("Release / Update Center") && main.includes("releaseCheck138"));
check("security panel", admin.includes("Security Panel") && admin.includes("Error Registry"));
if (checks.some(c=>!c.ok)) process.exit(1);
console.log(`admin-pro audit passed: ${checks.length} checks`);
